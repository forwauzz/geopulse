-- GPM-008
-- client_benchmark_configs: per-client GEO Performance Monitoring configuration.
-- Each row represents one client domain enrolled in the GEO Performance Monitoring service,
-- belonging to either a startup workspace or an agency account (never both).

CREATE TABLE public.client_benchmark_configs (
  id UUID PRIMARY KEY DEFAULT uuid_generate_v4(),

  -- workspace owner — exactly one must be non-null (enforced by check constraint below)
  startup_workspace_id UUID REFERENCES public.startup_workspaces(id) ON DELETE CASCADE,
  agency_account_id UUID REFERENCES public.agency_accounts(id) ON DELETE CASCADE,

  -- the client's measured domain
  benchmark_domain_id UUID NOT NULL REFERENCES public.benchmark_domains(id) ON DELETE RESTRICT,

  -- query configuration (topic + location → query_set_id is generated by admin at onboarding)
  topic TEXT NOT NULL,
  location TEXT NOT NULL,
  query_set_id UUID REFERENCES public.benchmark_query_sets(id) ON DELETE SET NULL,

  -- competitor tracking — brand names and/or domains from the client config
  competitor_list TEXT[] NOT NULL DEFAULT '{}',

  -- schedule and delivery
  cadence TEXT NOT NULL DEFAULT 'monthly',
  platforms_enabled TEXT[] NOT NULL DEFAULT ARRAY['chatgpt', 'gemini', 'perplexity'],
  report_email TEXT,

  -- extensible metadata
  metadata JSONB NOT NULL DEFAULT '{}'::jsonb,
  created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT now(),

  CONSTRAINT client_benchmark_configs_owner_check CHECK (
    (startup_workspace_id IS NOT NULL)::int + (agency_account_id IS NOT NULL)::int = 1
  ),
  CONSTRAINT client_benchmark_configs_cadence_check CHECK (
    cadence IN ('monthly', 'biweekly', 'weekly')
  )
);

-- one config per domain per workspace (partial unique indexes handle the nullable FK)
CREATE UNIQUE INDEX client_benchmark_configs_startup_domain_unique
  ON public.client_benchmark_configs (startup_workspace_id, benchmark_domain_id)
  WHERE startup_workspace_id IS NOT NULL;

CREATE UNIQUE INDEX client_benchmark_configs_agency_domain_unique
  ON public.client_benchmark_configs (agency_account_id, benchmark_domain_id)
  WHERE agency_account_id IS NOT NULL;

CREATE INDEX client_benchmark_configs_startup_workspace_idx
  ON public.client_benchmark_configs (startup_workspace_id, created_at DESC);

CREATE INDEX client_benchmark_configs_agency_account_idx
  ON public.client_benchmark_configs (agency_account_id, created_at DESC);

CREATE INDEX client_benchmark_configs_benchmark_domain_idx
  ON public.client_benchmark_configs (benchmark_domain_id);

CREATE INDEX client_benchmark_configs_query_set_idx
  ON public.client_benchmark_configs (query_set_id)
  WHERE query_set_id IS NOT NULL;

ALTER TABLE public.client_benchmark_configs ENABLE ROW LEVEL SECURITY;

CREATE TRIGGER client_benchmark_configs_updated_at
  BEFORE UPDATE ON public.client_benchmark_configs
  FOR EACH ROW EXECUTE FUNCTION update_updated_at();
